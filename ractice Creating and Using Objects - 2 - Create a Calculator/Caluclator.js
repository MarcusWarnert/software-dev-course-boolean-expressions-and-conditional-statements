// Calculator

//Step 1: Absolute Value Calculation
function absoluteValue(num1){
    return Math.abs(num1);
}
console.log(absoluteValue(-115));

//Step 2: Power Calculation
function powerCalculation(num1, exponent){
    return Math.pow(num1, exponent);
}
console.log(powerCalculation(-2, 3));

//Step 3: Square Root
function squareRoot(num1){
    return Math.sqrt(num1);
}console.log(squareRoot(25));

//Step 4: Maximum and Minimum
function maximumMimimum(num1, num2, num3){
    return Math.max(num1, num2, num3);
    return Math.min(num1, num2, num3);
}console.log(maximumMimimum(50, 1, 100));